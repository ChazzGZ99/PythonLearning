##Operadores Comparativos
print(3>4)
print(3<4)
print(3 >=  4)
print(3 <= 4)
print(3 == 4)
print(3 != 4)

## Operadoes Logicos ##
print(3 > 4 and "Hola" > "Python")
print(3 > 4 or "Hola" > "Python")
print(3 > 4 and "Hola" < "Python")
print(3 > 4 or "Hola" < "Python")

print(not(3 > 4))